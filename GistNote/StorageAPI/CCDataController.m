@implementation CCDataController

static CCDataController *_default = nil;
static dispatch_once_t safer;

//==============================
// Init
//==============================

- (instancetype)init
{
    if((self = [super init])) {
       [self initializeCoreData];
    }
    return self;
}

//==============================
// Get storage singleton
//==============================

+ (CCDataController *)getInstance
{
    if (_default != nil)
    {
        return _default;
    }
    
    dispatch_once(&safer, ^(void)
                  {
                      _default = [[CCDataController alloc] init];
                  });
    return _default;
}

